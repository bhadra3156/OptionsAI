def main() -> int:
    root = Path.cwd()
    if not (root / "package.json").exists():
        print("ERROR: run from project root (where package.json lives)")
        return 1

    target = root / TARGET
    if not target.exists():
        print(f"ERROR: {TARGET} not found. Has Phase B been installed?")
        return 1

    # Backup
    backup = target.with_suffix(target.suffix + ".bak")
    shutil.copyfile(target, backup)

    # Write new content
    b64 = "".join(CONTENT_B64)
    new_content = base64.b64decode(b64)
    target.write_bytes(new_content)

    old_size = backup.stat().st_size
    new_size = target.stat().st_size
    print(f"Patched: {TARGET}")
    print(f"  before: {old_size} bytes  (delta-based rules)")
