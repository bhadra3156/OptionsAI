def main() -> int:
    root = Path.cwd()
    if not (root / 'package.json').exists():
        print('ERROR: run from project root (where package.json lives)')
        return 1

    print('Installing Phase E (/signals UI) ...')
    print()
    for rel_path, b64_chunks in FILES.items():
        full_path = root / rel_path
        full_path.parent.mkdir(parents=True, exist_ok=True)
        if full_path.exists():
            backup = full_path.with_suffix(full_path.suffix + '.bak')
            shutil.copyfile(full_path, backup)
            note = '  (overwritten, backup saved)'
        else:
            note = ''
        b64 = ''.join(b64_chunks)
        full_path.write_bytes(base64.b64decode(b64))
        size = full_path.stat().st_size
        print(f'  wrote {rel_path}  ({size} bytes){note}')

    print()
    print(f'Done. {len(FILES)} files installed.')
    print()
    print('Next steps:')
    print('  1. npm run build')
    print('  2. git add . && git commit -m "Phase E: /signals page UI"')
    print('  3. git push')
    print('  4. Wait for Vercel deploy, then visit /signals on your live site')
    return 0

if __name__ == '__main__':
    sys.exit(main())