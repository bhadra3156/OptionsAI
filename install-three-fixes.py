SCANNER_PATH = Path('lib/scanner.ts')
OLD_LINE = "  'High IV Favourites': ['NFLX', 'COIN', 'PLTR', 'MSTR', 'RBLX', 'SNAP', 'UBER', 'HOOD', 'GME', 'BABA'],"
NEW_BLOCK = (
    "  'High IV Favourites': ['NFLX', 'COIN', 'PLTR', 'MSTR', 'RBLX', 'SNAP', 'UBER', 'HOOD', 'GME', 'BABA'],\n"
    "  'Cheap & Liquid': ['F', 'SOFI', 'NIO', 'AAL', 'JD'],"
)

def patch_scanner(root: Path) -> bool:
    target = root / SCANNER_PATH
    if not target.exists():
        print(f'  WARNING: {SCANNER_PATH} not found, skipping ticker addition')
        return False

    content = target.read_text(encoding='utf-8')
    if NEW_BLOCK in content:
        print(f'  scanner.ts: already patched (Cheap & Liquid present)')
        return True
    if OLD_LINE not in content:
        print(f'  WARNING: scanner.ts expected line not found. Manual fix needed.')
        print(f'  Expected: {OLD_LINE!r}')
        return False

    backup = target.with_suffix(target.suffix + '.bak')
    shutil.copyfile(target, backup)
    new_content = content.replace(OLD_LINE, NEW_BLOCK, 1)
    target.write_text(new_content, encoding='utf-8')
    print(f'  wrote {SCANNER_PATH}  (added Cheap & Liquid category)')
    return True

def main() -> int:
    root = Path.cwd()
    if not (root / 'package.json').exists():
        print('ERROR: run from project root (where package.json lives)')
        return 1

    print('Installing three fixes ...')
    print()

    # Full-file overwrites (with backup)
    for rel_path, b64_chunks in FILES.items():
        full_path = root / rel_path
        full_path.parent.mkdir(parents=True, exist_ok=True)
        if full_path.exists():
            backup = full_path.with_suffix(full_path.suffix + '.bak')
            shutil.copyfile(full_path, backup)
        b64 = ''.join(b64_chunks)
        content = base64.b64decode(b64)
        full_path.write_bytes(content)
        size = full_path.stat().st_size
        print(f'  wrote {rel_path}  ({size} bytes)')

    # Surgical scanner patch
    patch_scanner(root)

    print()
    print('Done.')
    print()
    print('Next steps:')
